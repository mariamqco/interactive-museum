class Piece{
    constructor(name){
        this.name = name;
        this.image = "";
        this.description = "";
    }

    getName(){
        return this.name;
    }

    getImage(){
        return this.image;
    }

    getDescription(){
        return this.description;
    }

    setName(name){
        this.name = name;
    }

    setImage(image){
        this.image = image;
    }

    setDescription(description){
        this.description = description;
    }
    
    


}